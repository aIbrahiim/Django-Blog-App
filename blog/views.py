from django.shortcuts import render
from django.contrib.auth.mixins import LoginRequiredMixin, UserPassesTestMixin
from django.views.generic import (
    ListView,
    DetailView,
    CreateView,
    UpdateView,
    DeleteView
)
from . models import Post

def home(request):
    return render(request, 'blog/home.html', context={'posts':Post.objects.all(), 'title':'Home'})

class PostListView(ListView):
    model = Post
    template_name = 'blog/home.html'
    context_object_name = 'posts'
    ordering = ['-date_posted']

class PostDetailView(DetailView):
    model = Post


class PostCreateView(LoginRequiredMixin, CreateView):
    model = Post
    fields = ['title', 'content']
    #when we try to create a blog it will tell us the author is null and we wanna the author of the post to be the current
    #logged in user and to do that we should override form_valid method
    #we need to override form_valid method
    def form_valid(self, form):
        #this is saying hey that form you trying to submit before you do that take this instance and set the author
        #equla to the current logged in user
        form.instance.author = self.request.user
        return super().form_valid(form)

class PostUpdateView(LoginRequiredMixin, UserPassesTestMixin, UpdateView):
    model = Post
    fields = ['title', 'content']

    def form_valid(self, form):
        form.instance.author = self.request.user
        return super().form_valid(form)

    def test_func(self):
        #we wanna get the post we wanna Update
        post = self.get_object()
        if self.request.user == post.author:
            return True
        return False


class PostDeleteView(LoginRequiredMixin, UserPassesTestMixin, DeleteView):
    model = Post
    success_url = '/'
    def test_func(self):
        #we wanna get the post we wanna Update
        post = self.get_object()
        if self.request.user == post.author:
            return True
        return False


